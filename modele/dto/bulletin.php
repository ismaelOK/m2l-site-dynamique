<?php
class Bulletin{
    protected string $idBulletin;
    
    protected int $mois;
    
    protected int $annee;

    protected string $bulletinPDF;

    public function __construct(string $idBulletin, int $mois, int $annee, string $bulletinPDF)
    {
        $this-string >idBulletin = string $idBulletin;
        $this-int >mois = int $mois;
        $this-int >annee = int $annee;
        $this-string >bulletinPDF = string $bulletinPDF;
    }


}